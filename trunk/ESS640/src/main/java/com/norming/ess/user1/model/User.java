public class User {
	private String asuserUserid;
	private String asuserUsername;
	private String asuserEmail;
	private String asuserPwd;
	
	public String getAsuserUserid() {
		return asuserUserid;
	}
	public void setAsuserUserid(String asuserUserid) {
		this.asuserUserid = asuserUserid;
	}
	public String getAsuserUsername() {
		return asuserUsername;
	}
	public void setAsuserUsername(String asuserUsername) {
		this.asuserUsername = asuserUsername;
	}
	public String getAsuserEmail() {
		return asuserEmail;
	}
	public void setAsuserEmail(String asuserEmail) {
		this.asuserEmail = asuserEmail;
	}

	public String getAsuserPwd() {
		return asuserPwd;
	}
	public void setAsuserPwd(String asuserPwd) {
		this.asuserPwd = asuserPwd;
	}
}
